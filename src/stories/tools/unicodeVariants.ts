type KeysOfUnion<T> = T extends T ? keyof T : never;
export type Font = keyof typeof fonts
export type Variant<F extends Font> = KeysOfUnion<typeof fonts[F]>

export default function getVariant<F extends Font>(text: string, font: F, variant: Variant<F>, verlan: boolean = false) {
	let list = text.split("")
	return (verlan ? list.reverse() : list).map((char) => {
		let id = fonts.normal.normal.indexOf(char);
		if (id !== -1) {
			let variants = fonts[font] as Record<typeof variant, string>
			let cr = Array.from(variants[variant])[id]
			return cr
		}
		return char
	}).join("")
}

